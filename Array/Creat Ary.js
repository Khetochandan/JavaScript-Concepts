// 1. Creating Arrays
let arr1 = [1, 2, 3];  // Literal syntax
console.log(arr1)  // o/p: [1, 2, 3]

let arr2 = new Array(5);  // Creates an array with 5 empty slots
console.log(arr2)    // [ <5 empty items> ]

let arr3 = Array.of(4, 5, 6);  // Creates an array [4, 5, 6]
console.log(arr3)  // [ 4, 5, 6 ]
