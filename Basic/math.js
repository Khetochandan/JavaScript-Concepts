// Returns the absolute value.
console.log(Math.abs(-5)) // 5

// Rounds a number up to the nearest intege
console.log(Math.ceil(5.5)) // 6

// Rounds a number to the nearest integer
console.log(Math.round(5.5)) // 6

// Rounds a number down to the nearest intege
console.log(Math.floor(5.5)) // 5

// Returns the largest numbe
console.log(Math.max(4,55,2,6,8,10,12)) // 55

// Returns the smallest numbe
console.log(Math.min(4,55,2,6,8,10,12)) // 2

// Returns a random number between 0 and 1
console.log(Math.random())

// Returns the square root
console.log(Math.pow(4,3)) // 64

// Returns the square root
console.log(Math.sqrt(64)) // 8

//Returns the integer part of a number (removes the decimal).
console.log(Math.trunc(6.4)) // 6





